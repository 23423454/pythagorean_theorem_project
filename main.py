def check_pythagorean(a, b, c):
    """Проверяет, выполняется ли теорема Пифагора для трёх чисел"""
    if a <= 0 or b <= 0 or c <= 0:
        return False  # Отрицательные и нулевые значения не подходят
    return a**2 + b**2 == c**2

if __name__ == "__main__":
    # Используем тестовые значения вместо input(), чтобы скрипт не ждал ввода
    test_cases = [(3, 4, 5), (5, 12, 13), (8, 15, 17), (7, 24, 25), (1, 2, 3), (0, 0, 0), (-3, 4, 5)]

    for a, b, c in test_cases:
        result = check_pythagorean(a, b, c)
        print(f"Треугольник со сторонами ({a}, {b}, {c}) {'удовлетворяет' if result else 'НЕ удовлетворяет'} теореме Пифагора")
